// This program converts the Celsius Temperature to Fahrenheit Temperature:
public class Task7 {
    public static void main (String[] args){
        System.out.println(task7(40));

    }
      static int task7(int celsius){
         return ((9*celsius)/5)+32;    // This is the formula that converts the Celsius to Fahrenheit.
                                       // task7 method will return the converted Celsius temperature
                                       // to Fahrenheit temperature.

    }
}